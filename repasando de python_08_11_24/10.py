# Lista de ejemplo
lista = ["manzana", "plátano", "cereza", "kiwi", "naranja"]

# Inicializamos un índice
i = 0

# Usamos un bucle while para imprimir cada elemento de la lista
while i < len(lista):
    print(lista[i])
    i += 1  # Aumentamos el índice en cada iteración



